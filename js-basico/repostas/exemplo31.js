function diaSemana(num) {
    switch (num) {
        case 1:
            console.log('domingo')
            break;

        case 2:
            console.log('segunda-feira')
            break;

        case 3:
            console.log('terca-feira')
            break;

        case 4:
            console.log('quarta-feira')
            break;

        case 5:
            console.log('quinta-feira')
            break;

        case 6:
            console.log('sexta-feira')
            break;

        case 7:
            console.log('sabado')
            break;
    
        default: console.log('numero invalido')
            break;
    }
}

diaSemana(1);
diaSemana(2);
diaSemana(3);
diaSemana(4);
diaSemana(5);
diaSemana(6);
diaSemana(7);
diaSemana(8);